import { Grid } from 'components/Grid'
import { Layout } from 'components/Layout'
import { useAuth } from 'context/authContext'
import Head from 'next/head'
import { useRouter } from 'next/router'
import { useEffect, useState } from 'react'
import { getAllUserAlbums } from 'services/albums'
import { Album } from 'components/Album'

export default function Home() {
  const [albums, setAlbums] = useState([])
  const [loadingAlbums, setLoadingAlbums] = useState(true)
  const { user, isLoading } = useAuth()
  const router = useRouter()

  useEffect(() => {
    if (!isLoading && !user) {
      router.push('/explore')
    }

    const fetchAlbums = async () => {
      const { data, error } = await getAllUserAlbums(user.id)
      if (error) {
        console.log(error)
      }
      setAlbums(data)
      setLoadingAlbums(false)
    }

    user && fetchAlbums()
  }, [isLoading, router, user])

  return (
    <div className='bg-zinc-900 text-white'>
      <Head>
        <title>Social Album | home </title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <Layout>
        <h1 className='text-2xl lg:text-3xl my-2 py-6 font-bold text-transparent bg-clip-text bg-gradient-to-r from-indigo-300 via-blue-800 to-purple-900'>
          Your Albums
        </h1>

        <Grid>
          {user && loadingAlbums
            ? Array(12)
                .fill('filled')
                .map((_e, i) => (
                  <div
                    key={i}
                    className='bg-zinc-700 w-full h-[150px] animate-pulse rounded'
                  ></div>
                ))
            : albums?.map((album) => <Album album={album} key={album.id} />)}
        </Grid>
      </Layout>
    </div>
  )
}
